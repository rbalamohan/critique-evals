SELECT
    COALESCE(w.NAME, 'Unknown') AS worker_name,
    COUNT(t.ID) AS total_tasks,
    SUM(CASE WHEN t.COMPLETION_DETAILS_SUCCESS = TRUE THEN 1 ELSE 0 END) AS successful_tasks,
    SUM(CASE WHEN t.COMPLETION_DETAILS_SUCCESS = TRUE THEN 1 ELSE 0 END)::FLOAT / NULLIF(COUNT(t.ID), 0) AS success_rate
FROM TASKS t
LEFT JOIN WORKERS w
    ON t.COMPLETING_WORKER_ID = w.ID
    AND w._FIVETRAN_DELETED = FALSE
JOIN ORGANIZATIONS o
    ON t.EXECUTOR_ID = o.ID
    AND o._FIVETRAN_DELETED = FALSE
WHERE
    o.NAME ILIKE '%Acme%'
    AND t._FIVETRAN_DELETED = FALSE
    AND t.COMPLETION_DETAILS_TIME >= DATEADD('day', -30, CURRENT_DATE())
GROUP BY
    COALESCE(w.NAME, 'Unknown')
ORDER BY
    total_tasks DESC
LIMIT 10