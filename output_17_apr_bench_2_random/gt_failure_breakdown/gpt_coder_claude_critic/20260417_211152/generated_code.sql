WITH org_tasks AS (
    SELECT
        t.COMPLETION_DETAILS_FAILURE_REASON
    FROM TASKS t
    INNER JOIN ORGANIZATIONS o
        ON t.EXECUTOR_ID = o.ID
    WHERE o.NAME ILIKE '%Acme%'
      AND o._FIVETRAN_DELETED = FALSE
      AND t._FIVETRAN_DELETED = FALSE
      AND t.COMPLETION_DETAILS_SUCCESS = FALSE
      AND t.COMPLETION_DETAILS_TIME >= DATEADD('month', -3, CURRENT_DATE())
),
reason_counts AS (
    SELECT
        COALESCE(COMPLETION_DETAILS_FAILURE_REASON, 'No reason recorded') AS FAILURE_REASON,
        COUNT(*) AS FAILURE_COUNT
    FROM org_tasks
    GROUP BY 1
),
total_failures AS (
    SELECT COUNT(*) AS TOTAL_FAILURES
    FROM org_tasks
)
SELECT
    r.FAILURE_REASON,
    r.FAILURE_COUNT,
    r.FAILURE_COUNT::FLOAT / NULLIF(t.TOTAL_FAILURES, 0) AS FAILURE_PERCENTAGE
FROM reason_counts r
CROSS JOIN total_failures t
ORDER BY r.FAILURE_COUNT DESC;