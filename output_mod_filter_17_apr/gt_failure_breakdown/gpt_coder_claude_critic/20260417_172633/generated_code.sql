WITH acme_org AS (
    SELECT
        ID
    FROM ORGANIZATIONS
    WHERE _FIVETRAN_DELETED = TRUE
      AND NAME ILIKE '%Acme%'
),
failure_counts AS (
    SELECT
        COALESCE(T.COMPLETION_DETAILS_FAILURE_REASON, 'No reason recorded') AS FAILURE_REASON,
        COUNT(*) AS FAILURE_COUNT
    FROM TASKS T
    INNER JOIN acme_org O
        ON T.EXECUTOR_ID = O.ID
    WHERE T._FIVETRAN_DELETED = FALSE
      AND T.COMPLETION_DETAILS_SUCCESS = FALSE
      AND T.COMPLETION_DETAILS_TIME >= DATEADD('month', -3, CURRENT_DATE())
    GROUP BY 1
),
total_failures AS (
    SELECT
        COUNT(*) AS TOTAL_FAILURES
    FROM TASKS T
    INNER JOIN acme_org O
        ON T.EXECUTOR_ID = O.ID
    WHERE T._FIVETRAN_DELETED = FALSE
      AND T.COMPLETION_DETAILS_SUCCESS = FALSE
      AND T.COMPLETION_DETAILS_TIME >= DATEADD('month', -3, CURRENT_DATE())
)
SELECT
    F.FAILURE_REASON,
    F.FAILURE_COUNT AS COUNT,
    F.FAILURE_COUNT::FLOAT / NULLIF(T.TOTAL_FAILURES, 0) AS PERCENTAGE_OF_TOTAL_FAILURES
FROM failure_counts F
CROSS JOIN total_failures T
ORDER BY F.FAILURE_COUNT DESC;