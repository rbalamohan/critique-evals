WITH acme_orgs AS (
  SELECT
    ID
  FROM ORGANIZATIONS
  WHERE _FIVETRAN_DELETED = FALSE
    AND NAME ILIKE '%Acme%'
),
filtered_failures AS (
  SELECT
    COALESCE(COMPLETION_DETAILS_FAILURE_REASON, 'No reason recorded') AS FAILURE_REASON
  FROM TASKS
  WHERE _FIVETRAN_DELETED = FALSE
    AND EXECUTOR_ID IN (SELECT ID FROM acme_orgs)
    AND COMPLETION_DETAILS_SUCCESS = FALSE
    AND COMPLETION_DETAILS_TIME >= DATEADD('month', -3, CURRENT_DATE())
),
total_failures AS (
  SELECT COUNT(*) AS TOTAL_COUNT
  FROM filtered_failures
)
SELECT
  f.FAILURE_REASON,
  COUNT(*) AS FAILURE_COUNT,
  COUNT(*)::FLOAT / NULLIF(t.TOTAL_COUNT, 0) AS FAILURE_PERCENTAGE
FROM filtered_failures f
CROSS JOIN total_failures t
GROUP BY
  f.FAILURE_REASON,
  t.TOTAL_COUNT
ORDER BY FAILURE_COUNT DESC;