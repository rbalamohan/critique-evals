WITH acme_org AS (
    SELECT
        ID
    FROM ORGANIZATIONS
    WHERE _FIVETRAN_DELETED = FALSE
      AND NAME ILIKE '%Acme%'
),
failed_tasks AS (
    SELECT
        COALESCE(T.COMPLETION_DETAILS_FAILURE_REASON, 'No reason recorded') AS FAILURE_REASON
    FROM TASKS T
    INNER JOIN acme_org O
        ON T.EXECUTOR_ID = O.ID
    WHERE T._FIVETRAN_DELETED = FALSE
      AND T.COMPLETION_DETAILS_SUCCESS = FALSE
      AND T.COMPLETION_DETAILS_TIME >= DATEADD('month', -3, CURRENT_DATE())
),
failure_totals AS (
    SELECT COUNT(*) AS TOTAL_FAILURES
    FROM failed_tasks
)
SELECT
    FT.FAILURE_REASON,
    COUNT(*) AS FAILURE_COUNT,
    COUNT(*)::FLOAT / NULLIF(MAX(TOT.TOTAL_FAILURES), 0) AS FAILURE_PERCENTAGE
FROM failed_tasks FT
CROSS JOIN failure_totals TOT
GROUP BY FT.FAILURE_REASON
ORDER BY FAILURE_COUNT DESC;