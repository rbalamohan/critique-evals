SELECT
  COALESCE(w.NAME, 'Unknown') AS worker_name,
  COUNT(*) AS total_tasks,
  SUM(CASE WHEN t.COMPLETION_DETAILS_SUCCESS THEN 1 ELSE 0 END) AS successful_tasks,
  SUM(CASE WHEN t.COMPLETION_DETAILS_SUCCESS THEN 1 ELSE 0 END)::FLOAT / NULLIF(COUNT(*), 0) AS success_rate
FROM TASKS t
JOIN ORGANIZATIONS o
  ON t.CREATOR_ID = o.ID
  AND o._FIVETRAN_DELETED = FALSE
LEFT JOIN WORKERS w
  ON t.COMPLETING_WORKER_ID = w.ID
  AND w._FIVETRAN_DELETED = FALSE
WHERE t._FIVETRAN_DELETED = FALSE
  AND o.NAME ILIKE '%Acme%'
  AND t.COMPLETION_DETAILS_TIME >= DATEADD('day', -30, CURRENT_DATE())
  AND t.COMPLETING_WORKER_ID IS NOT NULL
GROUP BY COALESCE(w.NAME, 'Unknown')
ORDER BY total_tasks DESC
LIMIT 10;