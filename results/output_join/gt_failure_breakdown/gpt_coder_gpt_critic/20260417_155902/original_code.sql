WITH acme_failures AS (
    SELECT
        COALESCE(TASKS.COMPLETION_DETAILS_FAILURE_REASON, 'No reason recorded') AS FAILURE_REASON
    FROM TASKS
    INNER JOIN ORGANIZATIONS
        ON TASKS.EXECUTOR_ID = ORGANIZATIONS.ID
    WHERE ORGANIZATIONS.NAME ILIKE '%Acme%'
      AND TASKS.COMPLETION_DETAILS_SUCCESS = FALSE
      AND TASKS.COMPLETION_DETAILS_TIME >= DATEADD('month', -3, CURRENT_DATE())
      AND TASKS._FIVETRAN_DELETED = FALSE
      AND ORGANIZATIONS._FIVETRAN_DELETED = FALSE
),
failure_counts AS (
    SELECT
        FAILURE_REASON,
        COUNT(*) AS FAILURE_COUNT
    FROM acme_failures
    GROUP BY FAILURE_REASON
),
total_failures AS (
    SELECT COUNT(*) AS TOTAL_FAILURES
    FROM acme_failures
)
SELECT
    fc.FAILURE_REASON,
    fc.FAILURE_COUNT,
    fc.FAILURE_COUNT::FLOAT / NULLIF(tf.TOTAL_FAILURES, 0) AS FAILURE_PERCENTAGE
FROM failure_counts fc
CROSS JOIN total_failures tf
ORDER BY fc.FAILURE_COUNT DESC;