WITH acme_org AS (
  SELECT ID
  FROM ORGANIZATIONS
  WHERE _FIVETRAN_DELETED = FALSE
    AND NAME ILIKE '%Acme%'
),
failure_counts AS (
  SELECT
    COALESCE(COMPLETION_DETAILS_FAILURE_REASON, 'No reason recorded') AS FAILURE_REASON,
    COUNT(*) AS FAILURE_COUNT
  FROM TASKS
  WHERE _FIVETRAN_DELETED = FALSE
    AND CREATOR_ID IN (SELECT ID FROM acme_org)
    AND COMPLETION_DETAILS_SUCCESS = FALSE
    AND COMPLETION_DETAILS_TIME >= DATEADD('month', -3, CURRENT_DATE())
  GROUP BY 1
),
total_failures AS (
  SELECT COUNT(*) AS TOTAL_FAILURES
  FROM TASKS
  WHERE _FIVETRAN_DELETED = FALSE
    AND EXECUTOR_ID IN (SELECT ID FROM acme_org)
    AND COMPLETION_DETAILS_SUCCESS = FALSE
    AND COMPLETION_DETAILS_TIME >= DATEADD('month', -3, CURRENT_DATE())
)
SELECT
  fc.FAILURE_REASON,
  fc.FAILURE_COUNT AS COUNT,
  fc.FAILURE_COUNT::FLOAT / NULLIF(tf.TOTAL_FAILURES, 0) AS PERCENTAGE_OF_TOTAL_FAILURES
FROM failure_counts fc
CROSS JOIN total_failures tf
ORDER BY fc.FAILURE_COUNT DESC;