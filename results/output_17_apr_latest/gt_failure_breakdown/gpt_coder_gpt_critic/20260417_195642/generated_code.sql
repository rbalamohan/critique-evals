WITH acme_org AS (
    SELECT ID
    FROM ORGANIZATIONS
    WHERE _FIVETRAN_DELETED = FALSE
      AND NAME ILIKE '%Acme%'
),
failures AS (
    SELECT
        COALESCE(t.COMPLETION_DETAILS_FAILURE_REASON, 'No reason recorded') AS FAILURE_REASON
    FROM TASKS t
    INNER JOIN acme_org o
        ON t.CREATOR_ID = o.ID
    WHERE t._FIVETRAN_DELETED = FALSE
      AND t.COMPLETION_DETAILS_SUCCESS = FALSE
      AND t.COMPLETION_DETAILS_TIME >= DATEADD('month', -3, CURRENT_DATE())
),
reason_counts AS (
    SELECT
        FAILURE_REASON,
        COUNT(*) AS FAILURE_COUNT
    FROM failures
    GROUP BY FAILURE_REASON
),
total_failures AS (
    SELECT COUNT(*) AS TOTAL_FAILURES
    FROM failures
)
SELECT
    rc.FAILURE_REASON,
    rc.FAILURE_COUNT,
    rc.FAILURE_COUNT::FLOAT / NULLIF(tf.TOTAL_FAILURES, 0) AS FAILURE_PERCENTAGE
FROM reason_counts rc
CROSS JOIN total_failures tf
ORDER BY rc.FAILURE_COUNT DESC, rc.FAILURE_REASON;