WITH acme_org AS (
    SELECT
        ID
    FROM ORGANIZATIONS
    WHERE _FIVETRAN_DELETED = FALSE
      AND NAME ILIKE '%Acme%'
),
failures AS (
    SELECT
        COALESCE(T.COMPLETION_DETAILS_FAILURE_REASON, 'No reason recorded') AS FAILURE_REASON
    FROM TASKS AS T
    INNER JOIN acme_org AS O
        ON T.EXECUTOR_ID = O.ID
    WHERE T._FIVETRAN_DELETED = FALSE
      AND T.COMPLETION_DETAILS_TIME >= DATEADD('month', -3, CURRENT_DATE())
      AND T.COMPLETION_DETAILS_SUCCESS = FALSE
),
failure_counts AS (
    SELECT
        FAILURE_REASON,
        COUNT(*) AS FAILURE_COUNT
    FROM failures
    GROUP BY FAILURE_REASON
),
total_failures AS (
    SELECT
        COUNT(*) AS TOTAL_FAILURES
    FROM failures
)
SELECT
    FC.FAILURE_REASON,
    FC.FAILURE_COUNT,
    FC.FAILURE_COUNT::FLOAT / NULLIF(TF.TOTAL_FAILURES, 0) AS PERCENTAGE_OF_TOTAL_FAILURES
FROM failure_counts AS FC
CROSS JOIN total_failures AS TF
ORDER BY FC.FAILURE_COUNT DESC, FC.FAILURE_REASON;