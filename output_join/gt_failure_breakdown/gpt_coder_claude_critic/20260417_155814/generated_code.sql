WITH org_tasks AS (
    SELECT
        t.COMPLETION_DETAILS_FAILURE_REASON
    FROM TASKS t
    JOIN ORGANIZATIONS o
        ON t.CREATOR_ID = o.ID
    WHERE o._FIVETRAN_DELETED = FALSE
      AND t._FIVETRAN_DELETED = FALSE
      AND o.NAME ILIKE '%Acme%'
      AND t.COMPLETION_DETAILS_TIME >= DATEADD('month', -3, CURRENT_DATE())
      AND t.COMPLETION_DETAILS_SUCCESS = FALSE
),
failure_counts AS (
    SELECT
        COALESCE(COMPLETION_DETAILS_FAILURE_REASON, 'No reason recorded') AS FAILURE_REASON,
        COUNT(*) AS FAILURE_COUNT
    FROM org_tasks
    GROUP BY 1
),
total_failures AS (
    SELECT COUNT(*) AS TOTAL_FAILURES
    FROM org_tasks
)
SELECT
    fc.FAILURE_REASON,
    fc.FAILURE_COUNT,
    fc.FAILURE_COUNT::FLOAT / NULLIF(tf.TOTAL_FAILURES, 0) AS FAILURE_PERCENTAGE
FROM failure_counts fc
CROSS JOIN total_failures tf
ORDER BY fc.FAILURE_COUNT DESC, fc.FAILURE_REASON;