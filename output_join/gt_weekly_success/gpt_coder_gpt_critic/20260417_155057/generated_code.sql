SELECT
  DATE_TRUNC('WEEK', t.COMPLETION_DETAILS_TIME) AS week_start_date,
  COUNT(*) AS total_tasks,
  SUM(CASE WHEN t.COMPLETION_DETAILS_SUCCESS = TRUE THEN 1 ELSE 0 END) AS successful_tasks,
  SUM(CASE WHEN t.COMPLETION_DETAILS_SUCCESS = FALSE THEN 1 ELSE 0 END) AS failed_tasks,
  SUM(CASE WHEN t.COMPLETION_DETAILS_SUCCESS = TRUE THEN 1 ELSE 0 END)::FLOAT / NULLIF(COUNT(*), 0) AS success_rate
FROM TASKS t
JOIN ORGANIZATIONS o
  ON t.CREATOR_ID = o.ID
WHERE o._FIVETRAN_DELETED = FALSE
  AND t._FIVETRAN_DELETED = FALSE
  AND o.NAME ILIKE '%Acme%'
  AND t.COMPLETION_DETAILS_TIME >= DATEADD('WEEK', -8, DATE_TRUNC('WEEK', CURRENT_DATE()))
  AND t.COMPLETION_DETAILS_TIME < CURRENT_DATE()
GROUP BY 1
ORDER BY 1;