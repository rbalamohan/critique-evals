SELECT
    DATE_TRUNC('week', t.COMPLETION_DETAILS_TIME) AS week_start_date,
    COUNT(*) AS total_tasks,
    COUNT(CASE WHEN t.COMPLETION_DETAILS_SUCCESS = TRUE THEN 1 END) AS successful_tasks,
    COUNT(CASE WHEN t.COMPLETION_DETAILS_SUCCESS = FALSE THEN 1 END) AS failed_tasks,
    COUNT(CASE WHEN t.COMPLETION_DETAILS_SUCCESS = TRUE THEN 1 END)::FLOAT / NULLIF(COUNT(*), 0) AS success_rate
FROM TASKS t
JOIN ORGANIZATIONS o
    ON t.EXECUTOR_ID = o.ID
WHERE o.NAME ILIKE '%Acme%'
    AND o._FIVETRAN_DELETED = FALSE
    AND t._FIVETRAN_DELETED = FALSE
    AND t.COMPLETION_DETAILS_TIME >= DATEADD('week', -8, DATE_TRUNC('week', CURRENT_DATE()))
    AND t.COMPLETION_DETAILS_TIME < CURRENT_DATE()
GROUP BY
    DATE_TRUNC('week', t.COMPLETION_DETAILS_TIME)
ORDER BY
    week_start_date ASC